//语言函数
function lang(str){
	if(!window.langs)return str;
	str=str.split(' ');
	var i,l,ret='',a=arguments;
	for(i=0,l=str.length;i<l;i++)ret+=langs[str[i]]?langs[str[i]]:str[i];
	str = ret.split('%s');
	if(a.length>1 && str.length>1){
		ret=str[0];
		for(i=1,l=str.length;i<l;i++)ret+=(i<a.length?a[i]:'')+str[i];
	}
	return ret;
}
